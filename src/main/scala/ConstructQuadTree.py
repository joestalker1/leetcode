class Node:
    def __init__(self, val, isLeaf, topLeft, topRight, bottomLeft, bottomRight):
        self.val = val
        self.isLeaf = isLeaf
        self.topLeft = topLeft
        self.topRight = topRight
        self.bottomLeft = bottomLeft
        self.bottomRight = bottomRight

class Solution:
    def construct(self, grid):
        if not grid:
            return grid
        n = len(grid)

        def construct_tree(r, c, size):
            if size == 2:
                if grid[r][c] == grid[r][c+1] == grid[r+1][c] == grid[r+1][c+1]:
                    a = True
                    if grid[r][c] == 0:
                        a = False
                    return Node(a, True, None, None, None, None)
                else:
                    topLeft = Node(True if grid[r][c] == 1 else False, True, None, None, None, None)
                    topRight = Node(True if grid[r][c+1] == 1 else False, True, None, None, None, None)
                    bottomLeft = Node(True if grid[r+1][c] == 1 else False, True, None, None, None, None)
                    bottomRight = Node(True if grid[r+1][c+1] == 1 else False, True, None, None, None, None)
                    return Node(False, False, topLeft, topRight, bottomLeft, bottomRight)
            else:
                new_size = size // 2
                topLeft = construct_tree(r, c, new_size)
                topRight = construct_tree(r, c+new_size, new_size)
                bottomLeft = construct_tree(r+new_size, c, new_size)
                bottomRight = construct_tree(r+new_size, c+new_size, new_size)
                if topLeft.val == topRight.val == bottomLeft.val == bottomRight.val and \
                        topLeft.isLeaf and topRight.isLeaf and bottomLeft.isLeaf and bottomRight.isLeaf:
                    return Node(topLeft.val, True, None, None, None, None)
                return Node(False, False, topLeft, topRight, bottomLeft, bottomRight)

        return construct_tree(0, 0, n)


