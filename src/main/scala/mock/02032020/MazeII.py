class Solution:
    def shortestDistance(self, maze, start, destination):
        INF = 10001
        q = []
        q.append([start[0], start[1]])
        dist = [[INF] * len(maze[0]) for _ in range(len(maze))]
        dist[start[0]][start[1]] = 0
        while q:
            r, c = q.pop(0)
            for dr, dc in [[-1, 0], [1, 0], [0, 1], [0, -1]]:
                count = 0
                r1, c1 = r + dr, c + dc
                while 0 <= r1 < len(maze) and 0 <= c1 < len(maze[0]) and maze[r1][c1] == 0:
                    r1 += dr
                    c1 += dc
                    count += 1
                r1 -= dr
                c1 -= dc
                if dist[r][c] + count < dist[r1][c1]:
                    dist[r1][c1] = dist[r][c] + count
                    q.append([r1, c1])

        return -1 if dist[destination[0]][destination[1]] == INF else dist[destination[0]][destination[1]]


