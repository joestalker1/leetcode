case class Islands(lands: List[Islands#Island], n: Int, m: Int) {
  type Island = List[(Int, Int)]

  @inline
  private def near(i1: List[(Int, Int)], i2: List[(Int, Int)]): Boolean = {
    val (l1, l2) = if (i1.length > i2.length) (i2, i1) else (i1, i2)
    var i = 0
    var f = false
    while (!f && i < l1.length) {
      val (x, y) = l1(i)
      if (x - 1 >= 0 && !f) {
        f = l2.exists { case (i, j) => i == (x - 1) && j == y }
      }
      if (x + 1 < n && !f) {
        f = l2.exists { case (i, j) => i == (x + 1) && j == y }
      }
      if (y - 1 >= 0 && !f) {
        f = l2.exists { case (i, j) => i == x && j == (y - 1) }
      }
      if (y + 1 < m && !f) {
        f = l2.exists { case (i, j) => i == x && j == (y + 1) }
      }
      i += 1
    }
    f
  }


  @inline
  def +(p: (Int, Int)): Islands = {
    val (x, y) = p
    var islandIndex: Option[(Island, Int)] = None
    if (x - 1 >= 0) {
      islandIndex = find((x - 1, y))
    }
    if (x + 1 < n && !islandIndex.isDefined) {
      islandIndex = find((x + 1, y))
    }
    if (y - 1 >= 0 && !islandIndex.isDefined) {
      islandIndex = find((x, y - 1))
    }
    if (y + 1 < m && !islandIndex.isDefined) {
      islandIndex = find(x, y + 1)
    }
    islandIndex.fold(Islands(lands :+ List(p), n, m)) { case (island, i) =>
      val nlands = lands.updated(i, p :: island)
      Islands(nlands, n, m)
    }
  }

  @inline
  private def find(p: (Int, Int)): Option[(Island, Int)] = {
    var i = 0
    var res: Option[(Island, Int)] = None
    while (i < lands.length && res.isEmpty) {
      if (lands(i).exists { case (x, y) => x == p._1 && y == p._2 }) res = Option(lands(i), i)
      i += 1
    }
    res
  }

  @inline
  private def tryUnion(list: Seq[Island]): Seq[Island] = {
    var ifFound = findUnion(list)
    var cur_list = list
    while (ifFound.isDefined) {
      val (i1, i2) = ifFound.get
      val fusedIsland = if (cur_list(i1).length < cur_list(i2).length) cur_list(i1) ++ cur_list(i2) else cur_list(i2) ++ cur_list(i1)
      val left = for (j <- 0 until cur_list.length if j != i1 && j != i2) yield cur_list(j)
      cur_list = fusedIsland +: left
      ifFound = findUnion(cur_list)
    }
    cur_list
  }

  def union(): Islands = {
    val list = tryUnion(lands)
    Islands(list.toList, n, m)
  }

  @inline
  private def findUnion(cur_lands: Seq[Island]): Option[(Int, Int)] = {
    var j = 0
    var ri: Option[(Int, Int)] = None
    while (j < (cur_lands.length - 1) && !ri.isDefined) {
      var i = j + 1
      while (i < cur_lands.length && !ri.isDefined) {
        if (near(cur_lands(j), cur_lands(i))) ri = Some(j, i)
        i += 1
      }
      j += 1
    }
    ri
  }
}


